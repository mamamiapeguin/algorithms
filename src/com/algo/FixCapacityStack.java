package com.algo;

/**
 * @author argomarz
 * @date 2021/4/12:26 下午
 */
public class FixCapacityStack<Item> {

    private Item[] a;
    private int N;

    public FixCapacityStack(int cap) {
        a = (Item[]) new Object[cap];
    }

    public boolean isEmpty() {
        return N == 0;
    }

    public boolean isFull() {
        return N == a.length;
    }

    public int size() {
        return N;
    }

    public void push(Item item) {
        a[N++] = item;
    }

    public Item pop() {
        return a[--N];
    }
}
